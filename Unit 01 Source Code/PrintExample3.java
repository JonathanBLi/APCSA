/*
 * Example of using escape sequences
 */

public class PrintExample3
{
	public static void main(String[] args) 
	{
	    // Display in weird ways
		System.out.println("let \"me\" ");
		System.out.print("hear\\a");
		System.out.print("Wolf\nPa\tck");
		System.out.println("\n");        // blank line
	}
}