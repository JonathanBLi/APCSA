/*
 *	Variables2.java
 *
 *	Demonstrate changing variable values
 */

public class Variables2
{
	public static void main(String[] args)
	{
		// Simple numeric variables
		int age;
		age = 17;
		
		double gpa = 3.95;
		
		System.out.print("age: ");
		System.out.println(age);
		
		System.out.print("GPA: ");
		System.out.println(gpa);
		System.out.println();
		
		// Change the contents stored in the variables
		age = 29;
		gpa = 4.3925;
		
		System.out.print("age: ");
		System.out.println(age);
		
		System.out.print("GPA: ");
		System.out.println(gpa);
		System.out.println();
	}
}